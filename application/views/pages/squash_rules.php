          <h3 class="panel-title">Main Rules</h3>
        </div>
        <div class="panel-body">
          <p>There are clear right of way rules on squash which are designed to prevent traffic jams.</p>
          <p><strong>A. KEEP THE GAME SAFE</strong></p>

          <p>Stop and play a let (i.e. play the rally over again) if any dangerous situation arises. For example, where you risk hitting your opponent with the racket or the ball, or risk barging your opponent.</p>

          <p>The rules provide penalties for dangerous and unsporting play.</p>

          <p><strong>B. GET OUT OF YOUR OPPONENTS WAY</strong></p>

          <p>The incoming player has the right of way. The outgoing player must provide an opponent with the "three freedoms"</p>
          <div class="squashed-list">
          <p>Freedom to move straight to the ball;</p>
          <p>Room for an unrestricted swing;</p>
          <p>A clear front wall to hit.</p>
          <p>A player must make every effort to avoid interference and provide an opponent with the above</p>
          <p>three freedoms:</p>
          <p>A stroke is awarded against a player failing to do this.</p>
          </div>
          <p>No. 1 above means: the incoming striker should not have to run around an opponent to get to the ball;</p>
          <p>and the outgoing player must not move back into the path of the incoming striker.</p>

          <p>After hitting, a player may have to circle back to position on the T to allow an opponent straight access to the ball.</p>
          <p>A player has no right to move directly back to the T or to stand on the T.</p>

          <p>No. 2 means; a player must provide room for a reasonable swing and for the striker to play any shot.</p>

          <p>No. 3 means; a player must allow the striker to play any part of the front wall.</p>
          <p>That is, the player cannot stand or recover in such a way that the striker’s shot is restricted to just part of the front wall.</p>

          <p><strong>C. NO BLOCKING, CROWDING OR RESTRICTING</strong></p>

          <p>In summary, the incoming striker has the right of way. An opponent may not block access, crowd the swing or restrict returns to just part of the front wall.</p>

          <p><strong>LET, NO LET, STROKE - HOW TO DECIDE?</strong></p>

          <strong>LET</strong>
          <br>
          <p>A let is where the rally is played again. A let should be allowed when:</p>
          <div class="squashed-list">
          <p>a player stops for safety reasons.</p>
          <p>the incoming striker suffers accidental interference and could have got to and returned the ball.</p>
          <p>the result of the rally is undecided or a shot in it has been appealed and is undecided</p>
          <p>Lets are also played if officials are undecided or (when there are no officials) players can’t agree</p>
          <p>whether a let or a stroke should be awarded.</p>
          </div>
          <strong>NO LET</strong>
          <br>
          <p>No let is where a player appeals for a let but the appeal is refused.</p>
          <p>This happens when the player could not have got to or returned the ball or where there was no interference.</p>

          <strong>STROKE</strong>
          <br>
          <p>A stroke is where the rally is awarded to a player as a penalty against the opponent.</p>
          <p>A stroke is awarded or given when:</p>
          <div class="squashed-list">
          <p>the incoming striker is in position to win the rally but suffers interference.</p>
          <p>the incoming striker would have struck the opponent with the ball going directly</p>
          <p>to ANY PART of the front wall.</p>
          <p>the outgoing player did not make every effort to clear</p>
          <p>A stroke is awarded if the interference was the fault of the outgoing player;</p>
          <p>for example, if the player has played a weak shot back to himself and is in the opponent’s way.</p>
          </div>

          <p>Where there are no officials, players must agree when a stroke should be awarded. Normally the offending player makes a sporting acknowledgement that he is at fault and agrees to give the opponent a stroke.</p>

          <p>So enjoy the game, keep it safe but most of all just have fun!</p>
